public class ExampleThree {

	public static void main(String[] args) {

		int x = 1;
		int y = 2;
		System.out.println(x + y + " are  values are"); // will add the values
		System.out.println("" + x + y + " are my values"); // concatenate "" with x
		System.out.println(x + " " + y + " are my values"); // concatenate x with " " to start
		System.out.println(x + (y + " are my values")); //bedmas applies
		
	}
}
